<?php
    // 自分の得意な言語で
    // Let's チャレンジ！！
    $input_lines = trim(fgets(STDIN));
    $i = $input_lines;
    $input_lines = trim(fgets(STDIN));
    $j = $input_lines;

    $check = 0;
    if(strlen($i)<$strlen($j){
        $k = $strlen($j);
    }else{
        $k = $strlen($i);
    }

    for($su=0;$su<$k;$su++){
        if($i[$su]=$j[$su]{
            $check ++;
        }

    }

    if($check=$su){
